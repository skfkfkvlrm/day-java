package dev.java.second;

public class AssignmentOperatorExample {
    public static void main(String[] args) {
        int a = 10;
        a += 5;     // a=15
        System.out.println(a); // a = a + 5
        a -= 3;     // a=12
        System.out.println(a);
        a *= 2;     // a=24
        System.out.println(a);
        a /= 4;     // a=6
        System.out.println(a);
        a %= 5;     // a=1
        System.out.println(a);

    }
}
